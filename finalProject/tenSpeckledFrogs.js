for (var i = 10; i > 0; i--) {
if (i === 2) {
  console.log("2 speckled frogs sat on a log eating some most delicious bugs.\nOne jumped in the pool where its nice and cool,\nthen there was 1 speckled frog.\n\n");
} else if (i > 1) {
    console.log(i + " speckled frogs sat on a log eating some most delicious bugs.\nOne jumped in the pool where its nice and cool,\nthen there were " + (i - 1) + " speckled frogs.\n\n");
  } else {
    console.log("1 speckled frog sat on a log eating some most delicious bugs.\nOne jumped in the pool where its nice and cool,\nthen there were no more speckled frogs!\n\n");
}
}

